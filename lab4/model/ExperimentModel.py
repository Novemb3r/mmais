class ExperimentModel:

    def __init__(self, Psi=None, F=None, H=None, A=None):
        self.Psi = Psi
        self.H = H
        self.F = F
        self.A = A

    def Psi(self, theta):
        return self.Psi(theta)

    def H(self, theta):
        return self.Psi(theta)

    def F(self, theta):
        return self.Psi(theta)

    def A(self, theta):
        return self.Psi(theta)
